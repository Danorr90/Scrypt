import hashlib
import random


#***********************************************************************************************************************************
#FONCTION PRINCIPALE
#***********************************************************************************************************************************
def main():
    press = input("Choisissez votre programme : \n 1) MD5 Convertion \n 2) PASSWORD GENERATOR \n 3) HASH de votre mot \n 4) Exit \n")

    if press == '1':
        return mdfive()
    if press == '2':
        return passGen()
    if press == '3':
        return HaSH()
    if press == '4':
        exit(print("A la prochaine !"))
    else:
        print("Veuillez choisir un num√©ro !")
        return main()

#***********************************************************************************************************************************
#MD5 HASH
#***********************************************************************************************************************************

def mdfive():
    data = input('Convertion md5 de votre mot : \n')
    output = hashlib.md5(data.encode('utf-8'))
    print('md5 = ',output.hexdigest(),"\n")
    return main()


#***********************************************************************************************************************************
