#***********************************************************************************************************************************
#SHA256 HASH
#***********************************************************************************************************************************

def HaSH():
    data = input('Convertion SHA256 de votre mot de passe :')
    output = hashlib.sha256(data.encode('utf-8'))
    print(output.hexdigest(), "\n")
    return main()

